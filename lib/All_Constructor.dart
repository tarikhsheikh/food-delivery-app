class AllConstructor {
  AllConstructor(
      { required this.nameOfFood,
       required this.describeFood,
       required this.calories,
       required this.pathOfImage,
       required this.price,
       required this.details,
       required this.ingredients,
       required this.time,
       required this.stars
      });
  String pathOfImage;
  String nameOfFood;
  String describeFood;
  String calories;
  String price;
  String details;
  String ingredients;
  String time;
  String stars;
}

